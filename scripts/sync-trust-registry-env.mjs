#!/usr/bin/env node
/**
 * Writes the archon-trust-registry .env from this repo's config/demo.json, so the TRQP
 * server points at the freshly-provisioned registry DID + group DIDs. Run after every
 * `npm run provision` / `npm run reset`.
 *
 * Usage: node scripts/sync-trust-registry-env.mjs <path-to-archon-trust-registry>
 *   e.g. node scripts/sync-trust-registry-env.mjs /opt/archon-trust-registry
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const target = process.argv[2];
if (!target) { console.error('usage: sync-trust-registry-env.mjs <path-to-archon-trust-registry>'); process.exit(1); }
if (!existsSync(target)) { console.error(`path not found: ${target}`); process.exit(1); }

const demoPath = join(ROOT, 'config', 'demo.json');
if (!existsSync(demoPath)) { console.error('config/demo.json not found — run `npm run provision` first'); process.exit(1); }
const d = JSON.parse(readFileSync(demoPath, 'utf-8'));

const port = process.env.TRUST_REGISTRY_PORT || '4260';
const env = [
  '# Generated by hatpro-archon scripts/sync-trust-registry-env.mjs',
  `REGISTRY_DID=${d.identities['hatpro-registry']}`,
  'REGISTRY_NAME=HATPro Trust Registry',
  `GATEKEEPER_URL=${d.node}`,
  'RESOURCE_FORMAT=both',
  `PORT=${port}`,
  'OWNER_GROUP=',
  `ADMIN_GROUP=${d.groups.admin}`,
  'MODERATOR_GROUP=',
  `MEMBER_GROUP=${d.groups.member}`,
  '',
].join('\n');

writeFileSync(join(target, '.env'), env);
console.log(`wrote ${join(target, '.env')}`);
console.log(`  REGISTRY_DID=${d.identities['hatpro-registry']}`);
console.log(`  GATEKEEPER_URL=${d.node}`);
